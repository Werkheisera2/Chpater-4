using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace EvenNum.cs
{
    class Program
    {
        static void Main(string[] args)
        {
            {
                for (int i = 2; i <= 101; i += 2)
                {
                    Console.Write(i);
                }
            }
        }
    }
}
